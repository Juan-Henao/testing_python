def find_max(lst: list) -> int:
    """ asd """

    return max(lst)


def find_min(lst: list) -> int:
    """ asd """

    return min(lst)


def sum_list(lst: list) -> int:
    """ asd """

    return sum(lst)


def product_list(lst: list) -> int:
    """ asd """

    result = 1
    for num in lst:
        result *= num
    return result


def sort_list(lst: list) -> list:
    """ asd """

    return sorted(lst)


def reverse_list(lst: list) -> list:
    """ asd """

    return lst[::-1]
